import { ChecklistItem, Genre } from "@/types";

let _counter = 0;
function uid(): string {
  _counter++;
  return `item-${Date.now()}-${_counter}-${Math.random().toString(36).slice(2, 7)}`;
}

function item(
  phase: "基礎" | "P1",
  category: string,
  name: string,
  criteria: string,
  genre: Genre = "共通"
): ChecklistItem {
  return {
    id: uid(),
    phase,
    category,
    name,
    criteria,
    checked: false,
    evaluator: "",
    genre,
  };
}

